package demo;

public class TwoArray_Example {

	public static void main(String[] args) 
	{
		int[][] a = {{10,20},{30,40},{5,60}};
		System.out.println("Two dimensional Array::");
		for(int i=0;i<3;i++) 
		{
			for(int j=0;j<2;j++) 
			{
				System.out.println(a[i][j]);
			}
		}
	}
}	

